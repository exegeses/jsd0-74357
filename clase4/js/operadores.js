// ==   vs  ===

console.log( 2 == "2" )
console.log( 2 === "2" )

// condicional if/else
let mensaje = ''
if(  3  > 10  ){
    mensaje = '3 es mayor que 10'
}
else{
    mensaje = '3 NO es mayor que 10'
}
console.log(mensaje)


/* ternario 
    (condicion) ? true : false
*/
let mensaje2 = ( 3 > 10 ) ? '3 es mayor que 10' : '3 NO es mayor que 10'
console.log(mensaje2)